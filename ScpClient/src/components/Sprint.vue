<template>
  <form>
    <div class="form-group">
      <label for="name">Name</label>
      <input v-model="name" id="name" type="text" class="form-control" />
    </div>
    <div class="form-group">
      <label for="startDate">Start Date</label>
      <input v-model="startDate" id="startDate" type="date" class="form-control" />
    </div>
    <div class="form-group">
      <label for="endDate">End Date</label>
      <input v-model="endDate" id="endDate" type="date" class="form-control" />
    </div>
    <button type="button" class="btn btn-primary" @click="saveForm">Save</button>
  </form>
</template>

<script lang="ts">
import Vue from 'vue';
import VueResource from 'vue-resource';
import { HttpResponse } from 'vue-resource/types/vue_resource';

export default Vue.extend({
  name: 'Sprint',
  data: () => {
    return {
      name: 'Sprint 1',
      startDate: null,
      endDate: null,
    };
  },
  props: {
    msg: String,
  },
  methods: {
    saveForm(): void {
      const formData = {
        name: (this.name as string),
        startDate: this.startDate,
        endDate: this.endDate,
      };
      // TODO: Use Axios instead
      this.$http.post('http://localhost:5000/api/sprints', formData).then((response: HttpResponse) => {
        console.log('test');
      }, (response: HttpResponse) => {
        console.log(response.json());
      });
    },
  },
  created(): void {
    this.$http.get('http://localhost:5000/api/sprints/1234').then((response: HttpResponse) => {
      console.log(response.json());
    }, (response: HttpResponse) => {
      // error callback
    });
  },
});
</script>

